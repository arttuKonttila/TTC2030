# ask the user for two numbers
first_number = int(input("Anna kokonaisluku: "))
second_number = int(input("Anna toinen kokonaisluku: "))

# count the total of the numbers
summa = first_number + second_number
# count the difference between the numbers
erotus = first_number - second_number
# multiply the numbers together
tulo = first_number * second_number
# divide the numbers with eachother
osamaara = first_number / second_number

# print everything out on the console
print("summa: ", summa)
print("erotus: ", erotus)
print("tulo: ", tulo)
print("osamäärä", osamaara)